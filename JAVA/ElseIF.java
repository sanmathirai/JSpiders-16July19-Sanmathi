class ElseIF
{
	public static void main(String args[])
	{
		int mark = 20;
		if(mark >= 70)
		{
			System.out.println("FCD");
		}
		else if(mark >= 60)
		{
			System.out.println("FC");
		}
		else if(mark >= 35)
		{
			System.out.println("SC");
		}
		else if(mark < 35)
		{
			System.out.println("Fail");
		}
	}
}
